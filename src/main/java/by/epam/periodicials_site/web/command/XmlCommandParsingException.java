package by.epam.periodicials_site.web.command;

public class XmlCommandParsingException extends RuntimeException{

	private static final long serialVersionUID = 4722103013537784999L;

	public XmlCommandParsingException() {
		super();
	}

	public XmlCommandParsingException(String message, Throwable cause) {
		super(message, cause);
	}

	public XmlCommandParsingException(String message) {
		super(message);
	}

	public XmlCommandParsingException(Throwable cause) {
		super(cause);
	}
	
	

}
