package by.epam.periodicials_site.entity;

public class Theme {
	
	private short id;
	private String name;
	
	public Theme() {
		super();
	}
	public short getId() {
		return id;
	}
	public void setId(short id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
